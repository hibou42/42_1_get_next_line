#include "get_next_line.h"

char	*get_next_line(int fd)
{
	static char	*str;

	if (fd < 0 || BUFFER_SIZE < 1)
		return (NULL);
	if (!str)
	{
		str = malloc(1 * sizeof(char));
		gnl_bzero(str, 1 * sizeof(char));
	}
	return (str);
}
